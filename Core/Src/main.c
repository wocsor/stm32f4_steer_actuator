void motor_set (int16_t amt, uint8_t enable)
{
  // differential signal with neutral at 50% duty cycle

  uint32_t duty = 32767;

  TIM2->CCR1 = duty + amt;
  TIM2->CCR2 = duty - amt; 

  // set the GPIO lines
  if (enable > 0){
    enable = 1;
  }
  HAL_GPIO_WritePin(GPIOA, CH1_EN_Pin, enable);
  HAL_GPIO_WritePin(GPIOA, CH2_EN_Pin, enable);

}

